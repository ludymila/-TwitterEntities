		<h2>Extrator de Entidades Nomeadas</h2>
		<p>O problema de identificação de entidades nomeadas (Named Entity Recognition - NER) consiste em encontrar palavras que ocorrem em
         um documento ou trecho de texto não estruturado e que façam referência a entidades do mundo real. Este problema tem sido estudado
         em vários contextos como identificação de nomes de pessoas, companhias e produtos em notícias, identificação de genes e proteínas
         em publicações biomédicas </p>
        <p>Neste projeto, enfocamos o problema de identificação de entidades nomeadas em mensagens curtas veiculadas através de redes sociais
         on-line como Twitter, Facebook, Linkedin, etc. A extração de informações contidas no corpus textual de redes sociais pode ajudar a
         responder de forma automática e efetiva questões como: Quem fala com quem sobre que assunto? Quem são os atores principais nas redes?
          Onde estão localizados? Que assuntos e tópicos emergem, se disseminam e desaparecem nos eco-sistemas sociais digitais? Que indivíduos
          e grupos promovem e suprimem estes assuntos e tópicos?  </p>

